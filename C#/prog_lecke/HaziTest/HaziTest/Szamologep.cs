using System;

namespace HaziTest
{
    class Szamologep
    {
        internal int oszead(int v1, int v2)
        {
            return v1 + v2;
        }

        internal int szoroz(int v1, int v2)
        {
            return v1 * v2;
        }

        internal int kivon(int v1, int v2)
        {
            return v1 - v2;
        }

        internal int negyzet(int v)
        {
            return v * v;
        }

        internal int oszt(int v1, int v2)
        {
            return v1 / v2;
        }
    }
}